# Python Program illustrating 
# numpy.inner() method 
  
import numpy as geek 
  
# Scalars 
product = geek.inner(5, 4) 
print("inner Product of scalar values : ", product) 
  
# 1D array 
vector_a = 2 + 3j
vector_b = 4 + 5j
  
product = geek.inner(vector_a, vector_b) 
print("inner Product : ", product)