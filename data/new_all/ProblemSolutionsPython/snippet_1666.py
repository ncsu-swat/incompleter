# numpy.char.find() method 
  
# importing numpy as geek
import numpy as geek
  
# input arrays  
in_arr = geek.array(['aAaAaA', 'baA', 'abBABba'])
print ("Input array : ", in_arr) 
  
# output arrays 
out_arr = geek.char.find(in_arr, sub ='A')
print ("Output array: ", out_arr)