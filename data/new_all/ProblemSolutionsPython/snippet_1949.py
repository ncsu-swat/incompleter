# numpy.char.count() method 
  
# importing numpy as geek
import numpy as geek
  
# input arrays  
in_arr = geek.array(['Sayantan', '  Sayan  ', 'Sayansubhra'])
print ("Input array : ", in_arr) 
  
# output arrays 
out_arr = geek.char.count(in_arr, sub ='an')
